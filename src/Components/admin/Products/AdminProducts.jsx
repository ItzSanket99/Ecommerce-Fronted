import React, { useState } from 'react'
import { MdAddShoppingCart } from "react-icons/md";
import { useDispatch, useSelector } from 'react-redux';
import Loader from "../../Shared/Loader.jsx"
import { FaBoxOpen } from "react-icons/fa";
import { adminProductTableColumn } from '../Helper/tableColumn.jsx';
import { DataGrid } from '@mui/x-data-grid';
import { useDashboardProductFilter } from '../../../Hooks/useProductFilter.js';
import Modal from "../../Shared/Modal.jsx"
import AddProductForm from './AddProductForm.jsx';
import DeleteModal from '../../Shared/DeleteModal.jsx';
import { deleteProduct } from '../../../Store/Actions/index.js';
import toast from 'react-hot-toast';
import ImageUploadForm from './ImageUploadForm.jsx';

const AdminProducts = () => {

  const dispatch = useDispatch();

  const {products, pagination} = useSelector((state) => state.Products);

  const {isLoading, errorMessage} = useSelector((state) => state.errors);
  const emptyProducts = !products || products?.length === 0;

  const [selectedProduct, setSelectedProduct] = useState('');
  const [currentPage, setCurrentPage] = useState(pagination?.pageNumber + 1 || 1);
  const [openUpdateModal,setOpenUpdateModal] = useState(false);
  const [openAddModal,setOpenAddModal] = useState(false);
  const [openDeleteModal,setOpenDeleteModal] = useState(false);
  const [openImageUploadModal,setOpenImageUploadModal] = useState(false);
  const [loader,setLoader] = useState(false);

  useDashboardProductFilter();
  
  const tableRecords = products?.map((item) =>{
    return {
      id: item.productId,
      productName:item.productName,
      description:item.description,
      quantity:item.quantity,
      price:item.price,
      discount:item.discount,
      specialPrice:item.specialPrice,
      image:item.image,
    }
  })

  const handleEdit = (product) => {
    setSelectedProduct(product);
    setOpenUpdateModal(true);

  }
  const handleDelete = (product) => {
    setSelectedProduct(product);
    setOpenDeleteModal(true);
  }
  const handleImageUpload = (product) => {
    setSelectedProduct(product);
    setOpenImageUploadModal(true);
  }
  const handleProductView = () => {

  }
  const handlePaginationChange = () => {

  }

  const onDeleteHandler = () => {
    dispatch(deleteProduct(setLoader, selectedProduct?.id, toast, setOpenDeleteModal));
  }

  return (
    <div>
      <div className='pt-6 pb-10 flex justify-end' >
        <button className='bg-custom-blue hover:bg-blue-800 text-white font-semibold py-2 px-4 flex items-center gap-2 rounded-md shadow-md transition-colors hover:text-slate-300 duration-300' 
        onClick={() => setOpenAddModal(true)}
        >
          <MdAddShoppingCart className='text-xl'/>
            Add Products   
        </button>
      </div>

      {!emptyProducts && (
        <h1 className='text-slate-800 text-3xl text-center font-bold pb-6 uppercase' >
          All Products
        </h1>
      )}
      {isLoading ? (
        <>
          <Loader />
          Loading...
        </>
      ) : (
        <>
          {emptyProducts ? (
            <div className='flex flex-col items-center justify-center text-gray-600 py-10'>
              <FaBoxOpen className='mb-3' size={30}/>
              No Product Exists
            </div>
          ):(
            <div className='max-w-full' >
              <DataGrid
                className='w-full'
                    rows={tableRecords}
                    columns={adminProductTableColumn(handleEdit,handleDelete,handleImageUpload,handleProductView)}
                    paginationMode='server'
                    rowCount={pagination?.totalElements || 0}
                    initialState={{
                      pagination: {
                        paginationModel: {
                          pageSize: pagination?.pageSize || 10,
                          page: currentPage - 1,
                        },
                      },
                    }}
                    onPaginationModelChange={handlePaginationChange}
                    disableRowSelectionOnClick
                    disableColumnResize
                    pageSizeOptions={[pagination?.pageSize || 10]}
                    pagination
                    paginationOptions={{
                      showFirstButton: true,
                      showLastButton: true,
                      hideNextButton: currentPage === pagination?.totalPages,
                    }}
                  />
            </div>
          )}
        </>
      )}

      <Modal 
        open={openUpdateModal || openAddModal}
        setOpen={openUpdateModal ? setOpenUpdateModal : setOpenAddModal}
        title={openUpdateModal ? 'Update Product' : 'Add Product'}
      >
        <AddProductForm
          setOpen={openUpdateModal ? setOpenUpdateModal : setOpenAddModal}
          product={selectedProduct}
          update={openUpdateModal}
        />
      </Modal>
      <Modal 
        open={openImageUploadModal}
        setOpen={setOpenImageUploadModal}
        title={"Add Product Image"}
      >
        <ImageUploadForm
          setOpen={setOpenImageUploadModal}
          product={selectedProduct}
          update={openUpdateModal}
        />
      </Modal>
      <DeleteModal
        open={openDeleteModal}
        setOpen={setOpenDeleteModal}
        loader={loader}
        title={"Delete Product"}
        onDeleteHandler={onDeleteHandler}
      >
        
      </DeleteModal>
    </div>
  )
}

export default AdminProducts